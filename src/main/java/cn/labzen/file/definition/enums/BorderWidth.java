             package cn.labzen.file.definition.enums;

/**
 * 边框宽度枚举
 * <p>
 * 用于定义单元格的边框粗细程度
 *
 * @author labzen
 */
public enum BorderWidth {

  /**
   * 无边框
   */
  NONE,

  /**
   * 细边框
   */
  THIN,

  /**
   * 中等边框
   */
  MEDIUM,

  /**
   * 粗边框
   */
  THICK,

  /**
   * 双边框
   */
  DOUBLE
}
